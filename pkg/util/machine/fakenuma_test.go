func TestFakeNUMAEnabled(t *testing.T) {
	noSlitFs := &afero.MemMapFs{}

	yesSlitYesFakeNUMAFs := &afero.MemMapFs{}
	afero.WriteFile(yesSlitYesFakeNUMAFs, "/sys/firmware/acpi/tables/SLIT", enabledFakeNUMASlitSample, 0400)

	yesSlitNoFakeNUMAFs := &afero.MemMapFs{}
	afero.WriteFile(yesSlitNoFakeNUMAFs, "/sys/firmware/acpi/tables/SLIT", diabledFakeNUMASlitSample, 0400)

	t.Parallel()
	type args struct {
		fs afero.Fs
	}
	tests := []struct {
		name    string
		args    args
		want    bool
		wantErr bool
	}{
		{
			name:    "no firwanre SLIT file means not fake numa",
			args:    args{fs: noSlitFs},
			want:    false,
			wantErr: true,
		},
		{
			name:    "SLIT having 8888 8888 is fake numa enabled",
			args:    args{fs: yesSlitYesFakeNUMAFs},
			want:    true,
			wantErr: false,
		},
		{
			name:    "SLIT NOT having 8888 8888 is fake numa diasabled",
			args:    args{fs: yesSlitNoFakeNUMAFs},
			want:    false,
			wantErr: false,
		},
	}
	for _, tt := range tests {
		tt := tt
		t.Run(tt.name, func(t *testing.T) {
			t.Parallel()
			got, err := FakeNUMAEnabled(tt.args.fs)
			if (err != nil) != tt.wantErr {
				t.Errorf("FakeNUMAEnabled() error = %v, wantErr %v", err, tt.wantErr)
				return
			}
			if got != tt.want {
				t.Errorf("FakeNUMAEnabled() = %v, want %v", got, tt.want)
			}
		})
	}
}
